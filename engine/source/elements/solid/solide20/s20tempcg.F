      SUBROUTINE S20TEMPCG(JFT,JLT,NPE, NC, NI, TEMP ,TEMPEL)
C-----------------------------------------------
C   I m p l i c i t   T y p e s
C-----------------------------------------------
#include      "implicit_f.inc"
C-----------------------------------------------
C   G l o b a l   P a r a m e t e r s
C-----------------------------------------------
#include      "mvsiz_p.inc"
#include      "param_c.inc"
C-----------------------------------------------
C   D u m m y   A r g u m e n t s
C-----------------------------------------------
      INTEGER NPE,NC(MVSIZ,NPE),JFT,JLT
C     REAL ou REAL*8
      my_real
     .   TEMP(*),NI(NPE),TEMPEL(*)
C-----------------------------------------------
C   C o m m o n   B l o c k s
C-----------------------------------------------
C-----------------------------------------------
C   L o c a l   V a r i a b l e s
C-----------------------------------------------
      INTEGER I,J
C     REAL ou REAL*8  
C ---  tempurature on integration point
       DO I=JFT,JLT 
         TEMPEL(I) = ZERO 
        DO J=1,NPE                                                  
         TEMPEL(I)= TEMPEL(I)+ NI(J) * TEMP(NC(I,J)) 
        ENDDO  
       ENDDO  
C
      RETURN
      END
