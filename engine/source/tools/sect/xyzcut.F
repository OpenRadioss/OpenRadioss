      SUBROUTINE XYZCUT(X,NODCUT)
C-----------------------------------------------
C   I m p l i c i t   T y p e s
C-----------------------------------------------
#include      "implicit_f.inc"
C-----------------------------------------------
C   D u m m y   A r g u m e n t s
C-----------------------------------------------
      my_real
     .   X(3,*)
      REAL R4
      INTEGER I,NODCUT
C-----------------------------------------------
      DO 100 I=1,NODCUT
      R4 = X(1,I)
      CALL WRITE_R_C(R4,1)
      R4 = X(2,I)
      CALL WRITE_R_C(R4,1)
      R4 = X(3,I)
      CALL WRITE_R_C(R4,1)
 100  CONTINUE
C-----------------------------------------------
      RETURN
      END
