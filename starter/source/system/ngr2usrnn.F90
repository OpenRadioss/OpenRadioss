        integer function ngr2usrnn(iu,igrnod,ngrnod)
! ----------------------------------------------------------------------------------------------------------------------
!                                                   Modules
! ----------------------------------------------------------------------------------------------------------------------
          use message_mod
          use groupdef_mod
! ----------------------------------------------------------------------------------------------------------------------
!                                                   Implicit none
! ----------------------------------------------------------------------------------------------------------------------
          implicit none
! ----------------------------------------------------------------------------------------------------------------------
!                                                   Arguments
! ----------------------------------------------------------------------------------------------------------------------
          integer,intent(in) :: iu 
          integer,intent(in) :: ngrnod 
          integer,intent(in) :: igrnod(ngrnod) 
! ----------------------------------------------------------------------------------------------------------------------
!                                                   Local Variables
! ----------------------------------------------------------------------------------------------------------------------
          integer :: i
! ----------------------------------------------------------------------------------------------------------------------
!                                                   Body
! ----------------------------------------------------------------------------------------------------------------------
         ngr2usrnn = 0
         if (iu /= 0) then
            i = 1
            do while (i <= ngrnod .and. ngr2usrnn == 0)
              if (iu == igrnod(i)) then
                  ngr2usrnn = i
              endif
              i = i + 1
            enddo
          endif

         return

       end function ngr2usrnn

      end module ngr2usrnn_mod
