      SUBROUTINE LINEAR_INTERP(TAB_X,TAB_Y,LTAB,X,Y)
C-----------------------------------------------
C   I m p l i c i t   T y p e s
C-----------------------------------------------
#include      "implicit_f.inc"
c!       IMPLICIT NONE
C-----------------------------------------------
C   D u m m y   A r g u m e n t s
C-----------------------------------------------
      my_real TAB_X(LTAB),TAB_Y(LTAB),X,Y
      INTEGER LTAB
C-----------------------------------------------
C   L o c a l   V a r i a b l e s
C-----------------------------------------------
      INTEGER XMIN,XMAX,XMID
      LOGICAL CONDITION
      my_real ALPHA
      
      XMIN=1
      XMAX=LTAB
      CONDITION=.TRUE.
      
      DO WHILE(CONDITION .eqv. .TRUE.)
c!       print*,'XMIN-XMAX',XMIN,XMAX
       IF(TAB_X(XMIN)==X)THEN
         XMAX=XMIN
         CONDITION=.FALSE.
       ELSEIF (TAB_X(XMAX)==X)THEN
         XMIN=XMAX
         CONDITION=.FALSE.
       ELSEIF (XMAX==(XMIN+1))THEN
         CONDITION=.FALSE.
       ELSE
         CONDITION=.TRUE.
         XMID=XMIN+(XMAX-XMIN)/2
         IF(TAB_X(XMID) > X)THEN 
            XMAX=XMID
         ELSE
            XMIN=XMID
         ENDIF
       ENDIF
      ENDDO   
      
c!      print*,'-->',XMIN,XMAX,TAB_X(XMIN),TAB_X(XMAX)
      IF(XMIN /= XMAX)THEN
        ALPHA = (TAB_Y(XMAX)-TAB_Y(XMIN))/(TAB_X(XMAX)-TAB_X(XMIN))
        Y = X* ALPHA + TAB_Y(XMIN)-ALPHA*TAB_X(XMIN)
      ELSE
        Y = TAB_Y(XMIN)
      ENDIF

      END
